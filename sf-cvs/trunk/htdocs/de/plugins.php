<p>
Audacity unterstützt VST Plugins für Macintosh und Windows - Betriebssysteme.
Es gibt Plugins für alle möglichen Filter, Eaqualizerfunktionen und vieles mehr! 
Eine Liste mit Links zu Downloadseiten oder Kaufmöglichkeiten findest du hier.
</p>

<p>
Die Plugin-Unterstützung ist noch nicht vollends ausgereift.
Wenn du ein VST Plugin findest, das nicht unterstützt wird, teile uns das bitte mit, damit wir den Fehler beheben können.
</p>

<p>
Um die VST Plugins nutzen zu können, genügt es ein Verzeichnis 
mit dem Namen <b>VST</b> im Audacity - Prgrammverzeichnis zu erstellen (falls nicht schon vorhanden)
und die Plugins hineinzukopieren/verschieben.
Beim nächsten Programmstart, werden die Plugins im "Effekte" - Menü erscheinen.
</p>

